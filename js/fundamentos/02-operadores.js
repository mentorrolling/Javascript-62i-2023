//Operadores

//Aritméticos
let numero1 = 22;
let numero2 = 2;

console.log(numero1 + numero2);
console.log(numero1 - numero2);
console.log(numero1 * numero2);
console.log(numero1 / numero2);
console.log(numero1 % numero2); //resto

//Unitarios
numero1++; // numero1 = numero1 + 1
numero1--; // numero1 = numero1 - 1

//Relacionales
console.log(numero1 == numero2);
console.log(numero1 > numero2);
console.log(numero1 < numero2);
console.log(numero1 >= numero2);
console.log(numero1 <= numero2);
console.log(numero1 != numero2); //distinto

//Negación
let verdadero = true;
console.log(!verdadero);

//lógicos
let estudio = false;
let practico = true;

console.log(estudio && practico); //AND
console.log(estudio || practico); //OR

